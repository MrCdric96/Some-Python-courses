"""Stack using Pyhton List
Stack is a LIFO data structure -- Last-inn first-out.
Use append() to push an item onto the stack.
Use pop() to remove an item.
"""

my_stack = list()
my_stack.append(4)
my_stack.append(7)
my_stack.append(12)
my_stack.append(19)
print(my_stack)

print(my_stack.pop())
print(my_stack.pop())
print(my_stack)

